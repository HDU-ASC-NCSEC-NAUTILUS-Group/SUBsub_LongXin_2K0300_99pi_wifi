#include "zf_driver_encoder.h"
#include "zf_driver_file.h"

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取带方向编码器的计数值（速度值）
// 参数说明     path        编码器设备文件路径，如 "/dev/zf_encoder_1"
// 返回参数     int16       编码器速度值（有符号，正负表示方向）
// 使用示例     int16 speed = encoder_get_count("/dev/zf_encoder_1");
// 备注信息     适用于带方向输出的编码器，使用PWM输入捕获方式
//-------------------------------------------------------------------------------------------------------------------
int16 encoder_get_count(const char *path)
{
    int16 dat;

    file_read_dat(path, &dat);

    return dat;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取正交编码器的累计计数值（读取后自动清零）
// 参数说明     path        编码器设备文件路径，如 "/dev/zf_encoder_quad_1"
// 返回参数     int32       编码器累计计数值（有符号，正负表示方向）
// 使用示例     int32 count = encoder_quad_get_count("/dev/zf_encoder_quad_1");
// 备注信息     适用于正交编码器（A/B相），使用GPIO中断方式实现4倍频解码
//              读取后内部计数器自动清零，返回值表示两次读取之间的脉冲数
//-------------------------------------------------------------------------------------------------------------------
int32 encoder_quad_get_count(const char *path)
{
    int32 dat;

    file_read_dat(path, &dat);

    return dat;
}
